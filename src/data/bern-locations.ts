export function normalizeBernLocation(value: string) {
  return value
    .toLowerCase()
    .trim()
    .replace(/ä/g, "ae")
    .replace(/ö/g, "oe")
    .replace(/ü/g, "ue")
    .replace(/é/g, "e")
    .replace(/è/g, "e")
    .replace(/ê/g, "e")
    .replace(/à/g, "a")
    .replace(/â/g, "a")
    .replace(/ç/g, "c")
    .replace(/ß/g, "ss")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9\s]/g, " ")
    .replace(/\s+/g, " ");
}

function unique(values: string[]) {
  const seenValues = new Set<string>();
  const uniqueValues: string[] = [];

  values.forEach((value) => {
    const normalizedValue = normalizeBernLocation(value);

    if (!normalizedValue || seenValues.has(normalizedValue)) {
      return;
    }

    seenValues.add(normalizedValue);
    uniqueValues.push(value);
  });

  return uniqueValues;
}

function queryContainsTerm(normalizedQuery: string, term: string) {
  const normalizedTerm = normalizeBernLocation(term);

  if (!normalizedTerm) {
    return false;
  }

  return ` ${normalizedQuery} `.includes(` ${normalizedTerm} `);
}

function getLocationAliases(location: string) {
  const aliases = exactLocationAliases[location] ?? [];
  const splitAliases = location
    .split(/[/-]/)
    .map((part) => part.trim())
    .filter((part) => part.length >= 3);

  return unique([location, ...aliases, ...splitAliases]);
}

function buildMatchCandidates() {
  const candidates: BernLocationMatch[] = [
    {
      targetLocation: "kanton bern",
      label: "Kanton Bern",
      allowedLocations: bernMunicipalities,
      scope: "canton",
    },
  ];

  bernLocationGroups.forEach((group) => {
    group.aliases.forEach((alias) => {
      candidates.push({
        targetLocation: alias,
        label: group.label,
        allowedLocations: group.locations,
        scope: group.scope,
      });
    });
  });

  bernMunicipalities.forEach((location) => {
    getLocationAliases(location).forEach((alias) => {
      candidates.push({
        targetLocation: alias,
        label: location,
        allowedLocations: [location],
        scope: "exact",
      });
    });
  });

  cantonBernAliases.forEach((alias) => {
    candidates.push({
      targetLocation: alias,
      label: "Kanton Bern",
      allowedLocations: bernMunicipalities,
      scope: "canton",
    });
  });

  return candidates
    .map((candidate) => ({
      ...candidate,
      targetLocation: normalizeBernLocation(candidate.targetLocation),
      allowedLocations: unique(candidate.allowedLocations).map(normalizeBernLocation),
    }))
    .filter((candidate) => candidate.targetLocation)
    .sort((firstCandidate, secondCandidate) => {
      if (secondCandidate.targetLocation.length !== firstCandidate.targetLocation.length) {
        return secondCandidate.targetLocation.length - firstCandidate.targetLocation.length;
      }

      const scopeRank: Record<BernLocationScope, number> = {
        canton: 4,
        region: 3,
        nearby: 2,
        exact: 1,
      };

      return scopeRank[secondCandidate.scope] - scopeRank[firstCandidate.scope];
    });
}

const bernMatchCandidates = buildMatchCandidates();

export const bernSearchLocations = unique([
  ...bernMunicipalities,
  ...bernLocationGroups.flatMap((group) => [group.label, ...group.aliases, ...group.locations]),
  ...Object.values(exactLocationAliases).flat(),
  ...cantonBernAliases,
]).map(normalizeBernLocation);

export function getBernLocationMatchForQuery(query: string): BernLocationMatch | null {
  const normalizedQuery = normalizeBernLocation(query);

  if (!normalizedQuery) {
    return null;
  }

  const match = bernMatchCandidates.find((candidate) =>
    queryContainsTerm(normalizedQuery, candidate.targetLocation)
  );

  if (!match) {
    return null;
  }

  return match;
}
